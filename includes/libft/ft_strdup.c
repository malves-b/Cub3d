#include "../libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	int		i;
	char	*s;

	len = ft_strlen(src);
	s = (char *)malloc(sizeof(char) * (len + 1));
	if (!s)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		s[i] = src[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
