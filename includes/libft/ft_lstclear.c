#include "../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tmp;
	t_list	*del_node;

	if (*lst)
	{
		tmp = *lst;
		*lst = NULL;
		while (tmp)
		{
			del_node = tmp;
			tmp = tmp->next;
			del(del_node->content);
			free(del_node);
		}
	}
}
